quote = "beautiful patience is at the first strike of a calamity"
#upper usage
print(quote.upper())

#lower usage
print(quote.lower())

#capitalize usage
print(quote.capitalize())

#title usage
print(quote.title())

#join usage
quote2 = '-'.join(quote)
print(quote2)

#split usage
print(quote.split())

#count usage
print(quote.count('i'))
print(quote.count('t'))

#index usage
print(quote.index('beautiful'))
print(quote.index('patience'))
print(quote.index('first'))
print(quote.index('strike'))
print(quote.index('calamity'))

#len usage
print(len(quote))

