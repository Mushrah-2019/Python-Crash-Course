#Task 1:

#Addition
num1 = 10
num2 = 20
add_two_num = num1 + num2
print (add_two_num)

#Substraction
sub_two_num = num1 - num2
print (sub_two_num)

#Multiplication
mult_two_num = num1 * num2
print (mult_two_num)

#Division
div_two_num = num1 / num2
print (div_two_num)

#Modulus
mod_two_num = num1 % num2
print (mod_two_num)

#Exponentiation
exp_two_num = num1 ** num2
print (exp_two_num)

#Floor division
floor_two_num = num1 // num2
print (floor_two_num)

print(f"The addition of 10 and 20 is: {add_two_num}, the substraction is: {sub_two_num}, the multiplication is {mult_two_num}, the division is: {div_two_num}, ")
